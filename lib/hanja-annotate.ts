// 한자를 "한글(한자)" 형태로 렌더링하기 위한 후처리.
//   예: "丁火와 庚金이 만나는 흐름" → "정화(丁火)와 경금(庚金)이 만나는 흐름"
//   이미 "정화(丁火)" 형태로 들어온 경우는 건드리지 않음 (괄호 lookahead/behind 체크).

const COMPOUND: Record<string, string> = {
  // 일간 + 오행 (10간 + 5행 조합)
  甲木: "갑목", 乙木: "을목",
  丙火: "병화", 丁火: "정화",
  戊土: "무토", 己土: "기토",
  庚金: "경금", 辛金: "신금",
  壬水: "임수", 癸水: "계수",
  // 자주 등장하는 일주
  丁巳: "정사", 庚申: "경신",
  // 합/충 한자 단어
  天干: "천간", 地支: "지지",
  五行: "오행", 日柱: "일주",
};

const SINGLE: Record<string, string> = {
  // 오행
  木: "목", 火: "화", 土: "토", 金: "금", 水: "수",
  // 천간
  甲: "갑", 乙: "을", 丙: "병", 丁: "정", 戊: "무",
  己: "기", 庚: "경", 辛: "신", 壬: "임", 癸: "계",
  // 지지
  子: "자", 丑: "축", 寅: "인", 卯: "묘", 辰: "진",
  巳: "사", 午: "오", 未: "미", 申: "신", 酉: "유",
  戌: "술", 亥: "해",
  // 합/충 등
  合: "합", 沖: "충", 刑: "형", 害: "해", 破: "파",
  陰: "음", 陽: "양", 命: "명", 局: "국", 氣: "기",
  運: "운", 煞: "살",
};

export function annotateHanja(text: string): string {
  // 1+ 연속 한자, 단 앞에 `(` 없고 뒤에 `(한글` 없을 때만.
  return text.replace(
    /(?<!\()[一-鿿]+(?!\s*\([가-힣])/g,
    (run) => {
      const compound = COMPOUND[run];
      if (compound) return `${compound}(${run})`;
      const reading = Array.from(run)
        .map((c) => SINGLE[c] ?? c)
        .join("");
      if (reading === run) return run; // 매핑 없음 → 원본 유지
      return `${reading}(${run})`;
    },
  );
}
